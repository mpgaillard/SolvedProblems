class AddMultiply {
public:
	vector <int> makeExpression(int y) {
		vector<int> res(3);
		for (int i = -1000; i <= 1000; i++)
		{
			if(i == 0 || i == 1) continue; 
			for(int j= -1000; j<= 1000; j++){
				if(j==0 || j == 1) continue;
				int diff = y-(i*j);
				if(diff >= -1000 && diff <= 1000 && diff != 0 && diff != 1){
					res[0] = i; res[1] = j; res[2] = diff;
					return res;
				}
			}
		}
		return res;
	}
};


