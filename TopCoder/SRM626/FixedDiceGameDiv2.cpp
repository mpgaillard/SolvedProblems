class FixedDiceGameDiv2 {
public:
	double getExpectation(int a, int b) {
		int wins=0;
		double pW;
		for (int i = 2; i <= a; i++)
		{
			wins += min(i-1, b);
		}
		pW = wins/double(a*b);


		double res = 0.0;
		for(int i=2; i<= a; i++){
			res += double(i)*( min(i-1, b)/double(a*b) )/pW; 
		}
		return res;
	}
};


